using System;
using System.Collections.Generic;
using System.Linq;
using System.Threading.Tasks;

namespace atividade_classes_e_objetos
{
    public class Calculadora
    {
                public float v1;
    
                public float v2;

                public float Somar()
                {
                    return v1 + v2;
                }
                public float Subtrair()
                {
                    return v1 - v2;
                }
                public float Multiplicar()
                {
                    return v1 * v2;
                }
                public float Dividir()
                {
                    return v1 / v2;
                }
    }
}
